package sudoku;

/**
 *
 * @author onur
 */
public class Order {
    private final int x, y, value;
    public Order(int x, int y, int value){
        this.value=value;
        this.x=x;
        this.y=y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getValue() {
        return value;
    }
    
}
